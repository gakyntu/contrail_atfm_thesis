function [p] = pressure_standard_atmosphere(h,status)

% altitude h is to be expressed in km

switch status

    case 'ISA'

        T0=288.15;
        p0=1013.25;
        p11=226.32;
        h11=11;
        T11=216.65;
        p=(h<=h11).*(p0*(1-0.0065*h*1e3/T0).^(5.2561))+(h>h11).*(p11*exp(-9.81*(h-h11)*1e3/(287.04*T11)));

    case 'High-Latitude Summer'

        p=(h<=10).*(1008.0278-113.2494.*h+3.9408.*h.^2) + (h>10).*(h<=72).*(269.6138*exp(-0.140*(h-10))) + (h>72).*(h<=100).*0.0458.*exp(-0.165*(h-72));

    case 'High-Latitude Winter'

        p=(h<=10).*(1010.8828-122.2411.*h+4.554.*h.^2) + (h>10).*(h<=72).*(243.8718*exp(-0.147*(h-10))) + (h>72).*(h<100).*0.0269.*exp(-0.150*(h-72));

    case 'Mid-Latitude Summer'

        p=(h<=10).*(1012.8186-111.5569.*h+3.8646.*h.^2) + (h>10).*(h<=72).*(283.7096*exp(-0.147*(h-10))) + (h>72).*(h<=100).*0.0312.*exp(-0.165*(h-72));

    case 'Mid-Latitude Winter'

        p=(h<=10).*(1018.8627-124.2954.*h+4.8307.*h.^2) + (h>10).*(h<=72).*(258.9787*exp(-0.147*(h-10))) + (h>72).*(h<=100).*0.0285.*exp(-0.155*(h-72));

    case 'Low-Latitude Annual'

        p=(h<=10).*(1012.0306-109.0338.*h+3.6319.*h.^2) + (h>10).*(h<=72).*(284.8526*exp(-0.147*(h-10))) + (h>72).*(h<=100).*(0.0314*exp(-0.165*(h-72)));

end
        

end